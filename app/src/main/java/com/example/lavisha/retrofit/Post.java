package com.example.lavisha.retrofit;

public class Post {

    String title;
    String image;
    String platform;
    String date;
    String id;

    public String getId() {
        return id;
    }

    public String getTitle() {
        return title;
    }


    public String getImage() {
        return image;
    }

    public String getPlatform() {
        return platform;
    }


    public String getDate() {
        return date;
    }

}
